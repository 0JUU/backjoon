# 문제: N개의 숫자가 공백 없이 쓰여있다. 
#       이 숫자를 모두 합해서 출력하는 프로그램을 작성하시오.
#########################################################
# 입력: 첫째 줄에 숫자의 개수 N (1 ≤ N ≤ 100)이 주어진다. 
#       둘째 줄에 숫자 N개가 공백없이 주어진다.
# 출력: 입력으로 주어진 숫자 N개의 합을 출력한다.
#########################################################
# 예제 입력1: 1
#            1
# 예제 출력1: 1
#----------------------------
# 예제 입력1: 25
#            7000000000000000000000000
# 예제 출력1: 7
#########################################################
# 알고리즘 분류: 수학, 구현, 문자열
#########################################################
# 구현코드:
N=int(input())
M=input()
s=0
if N==len(M):
    for i in M:
        s+=int(i)
    print(s)
