# 문제: 0보다 크거나 같은 정수 N이 주어진다. 이때, N!을 출력하는 프로그램을 작성하시오.
##############################################################################
# 입력: 첫째 줄에 정수 N(0 ≤ N ≤ 12)이 주어진다.
# 출력: 첫째 줄에 N!을 출력한다.
##############################################################################
# 예제 입력1: 10
# 예제 출력1: 3628800
#-------------------
# 예제 입력2: 0
# 예제 출력2: 1
##############################################################################
# 알고리즘 분류: 수학, 구현
##############################################################################
# 구현코드:
a=1
for i in range(1,int(input())+1):
    a*=i
print(a)
